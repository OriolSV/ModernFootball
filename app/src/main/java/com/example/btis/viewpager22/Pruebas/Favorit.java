public class Favorit {

    private int escutFav;
    private String nomFav;
    private int numero;

    public Favorit(int escutFav,String nomFav, int numero ) {

        this.escutFav = escutFav;
        this.nomFav = nomFav;
        this.numero = numero;
        }

    public int getEscutFav() {
        return escutFav;
    }

    public String getNomFav() {
        return nomFav;
    }


}